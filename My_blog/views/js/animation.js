var log = document.getElementById('icon_log');
log.onmouseover = function(){
    document.getElementById('table_log').style.display = "block";
}
document.getElementById('table_log').onmouseover = function(){
    document.getElementById('table_log').style.display = "block";
}
log.onmouseout= function(){
    document.getElementById('table_log').style.display = "none";
}
document.getElementById('table_log').onmouseout = function(){
    document.getElementById('table_log').style.display = "none";
}